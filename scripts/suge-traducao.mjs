function translateWord(word) {
    if (word.includes('_')) {
        return word.split('_').map(part => translations[part] || part).join('_');
    }

    if (translations[word]) return translations[word];

    const parts = word.replace(/([A-Z])/g, ' $1').trim().split(' ');
    if (parts.length > 1) {
        return parts.map(part => translations[part] || part).join('');
    }

    return word;
}

function processFile() {
    const content = fs.readFileSync(MAPPING_FILE, 'utf-8');
    const lines = content.split('\n');
    const newLines = lines.map(line => {
        const parts = line.split('=');
        if (parts.length < 2) return line;

        const oldName = parts[0].trim();
        const currentMapping = parts.slice(1).join('=').trim();

        if (oldName.startsWith('__')) return line;

        if (currentMapping.length === 0) {
            const translated = translateWord(oldName);
            if (translated !== oldName) {
                return `${oldName} = ${translated}`;
            }
        }
        return line;
    });

    fs.writeFileSync(MAPPING_FILE, newLines.join('\n'));
    console.log('Sugestões de tradução aplicadas ao name.txt');
}

processFile();
